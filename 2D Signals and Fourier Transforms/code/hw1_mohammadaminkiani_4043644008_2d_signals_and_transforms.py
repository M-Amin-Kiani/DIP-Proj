    surf = ax.plot_surface(
        Xg, Yg, Z,
        cmap=cmap,
        linewidth=0,
        antialiased=True,
        rstride=2,
        cstride=2
    )
    ax.set_title(title, fontsize=11)
    ax.set_xlabel(xlabel)
    ax.set_ylabel(ylabel)
    ax.set_zlabel(zlabel)
    ax.view_init(elev=35, azim=-135)
    return surf

