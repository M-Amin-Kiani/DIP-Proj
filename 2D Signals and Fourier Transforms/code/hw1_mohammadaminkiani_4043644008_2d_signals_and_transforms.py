def normalize_for_plot(z):
    z = np.asarray(z, dtype=float)
    zmin = np.min(z)
    zmax = np.max(z)
    if np.isclose(zmax - zmin, 0):
        return np.zeros_like(z)
    return (z - zmin) / (zmax - zmin)

